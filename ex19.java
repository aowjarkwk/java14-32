public class ex19 {

	public static void main(String[] args) {
		//연습문제
		//int i 정수형 변수를 선언하고,
		// 10 으로 초기화 한 다음에,
		// ++ 연산자 만을 사용하여, 15를 출력하시오.
		// -- 연산자 만을 이용하여, 8을 출력하시오.
		int i = 10;
		i++;
		i++;
		i++;
		i++;
		i++;
		System.out.println(i);
		i--;
		i--;
		i--;
		i--;
		i--;
		i--;
		i--;
		System.out.println(i);

	}

}

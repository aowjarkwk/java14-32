package examples;

public class ex18 {

	public static void main(String[] args) {
		
		// 자바 연산자
		// 이항연산자 : 산비논대
		                                      //비트단위 연산자
		// 단항 : ++ -- !(논리반전) (타입)      ~
		// 산술 : + - * / %                     << >> >>>
		// 비교 : < > <= >= == !=   
		// 논리 : && ||                         & | ^(xor)
		// 삼항 : ? : 
		// 대입 : =  += -= *= /= %= 

		//단항연상자 ++ -- ! 
		
		int i = 10;
		i++; // i = i + 1;
		System.out.println(i);
		
		i--; // i = i - 1;
		System.out.println(i);

		System.out.println(!true);
		System.out.println(!false);
	}

}

package examples;

public class ex24 {

	public static void main(String[] args) {
		//비교연산자
		// == != > < <= >= 
		
		int i = 10;
		int j = 20;
		System.out.println(i==j);
		System.out.println(i!=j);
		System.out.println(i<j);
		System.out.println(i>j);
		System.out.println(i>=j); // (i>j) || (i=-j)
		System.out.println(i<=j);

	}

}
